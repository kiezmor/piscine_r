int	ft_count_if(char **tab, int (*f)(char*))
{
	int	v;
	int	p;

	v = 0;
	p = 0;
	while (tab[p])
	{
		if (f(tab[p]) == 1)
			v++;
		p++;
	}
	return (v);
}
